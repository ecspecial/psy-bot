def send_api_request(prompt, api_key):
    APIBody = {
        "model": "gpt-3.5-turbo",
        "messages": [
            {"role": "user", "content": prompt}
        ]
    }

    response = requests.post(
        "https://api.openai.com/v1/chat/completions",
        headers={
            "Content-Type": "application/json",
            "Authorization": f"Bearer {api_key}"
        },
        data=json.dumps(APIBody)
    )

    return response

def get_gpt_response(user_question):
    formatted_prompt = prompt_template.format(user_question=user_question)
    retry_count = 0

    while retry_count < 5:
        selected_api_key = random.choice(API_KEYS)
        response = send_api_request(formatted_prompt, selected_api_key)
        data = response.json()

        # Check for specific error messages
        if 'error' in data:
            error_message = data['error']['message']
            if 'deactivated' in error_message or 'insufficient_quota' in error_message or 'invalid_api_key' in error_message:
                retry_count += 1
                continue
            else:
                break  # If the error is not one of the specified types, break the loop
        else:
            break  # If no error, break the loop

    if 'error' in data:
        print("Не удалось получить ответ после 5 попыток.")
        return None
    else:
        message_content = data['choices'][0]['message']['content']
        print(message_content)
        return message_content

