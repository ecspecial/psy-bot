def handle_user_response(chat_id, message):
    if chat_id in user_states:
        current_state = user_states[chat_id]
        subtype, question_idx = current_state["subtype"], current_state["question_idx"]
        
        # Check if it's time for recommendations
        if question_idx >= len(questions_for_subtypes[subtype]["questions"]):
            bot.send_message(chat_id, questions_for_subtypes[subtype]["recommendations"])
            del user_states[chat_id]  # Clear the state after completion
        else:
            next_question = questions_for_subtypes[subtype]["questions"][question_idx]
            bot.send_message(chat_id, next_question)
            current_state["question_idx"] += 1
    else:
        # Handle case where there is no state for the user
        bot.send_message(chat_id, "Для обсуждения нового вопроса выбери меню справа и нажми кнопку \"Новый вопрос\"")

@bot.message_handler(func=lambda message: True)
def user_response_handler(message):
    handle_user_response(message.chat.id, message.text)

def process_question_step(message):
    gpt_response = get_gpt_response(message.text).lower()
    matched_main_type = None
    for main_type in question_types:
        if main_type.lower() in gpt_response:
            matched_main_type = main_type
            break

    if matched_main_type:
        specific_response = get_specific_type_response(message.text, matched_main_type).lower()
        matched_specific_type = None
        for specific_type in specific_subtypes[matched_main_type]:
            if specific_type.lower() in specific_response:
                matched_specific_type = specific_type
                break

        if matched_specific_type:
            user_states[message.chat.id] = {"subtype": matched_specific_type, "question_idx": 0}
            bot.send_message(message.chat.id, f"Я считаю мы можем обсудить следующую тематику: {matched_specific_type}")
            markup = InlineKeyboardMarkup()
            markup.row_width = 2
            markup.add(InlineKeyboardButton("Не против", callback_data='yes'),
                        InlineKeyboardButton("Против", callback_data='no'))
            bot.send_message(message.chat.id, "Ты не против, если я задам тебе несколько уточняющих вопросов, чтобы лучше разобраться в ситуации и помочь тебе?", reply_markup=markup)

        else:
            bot.send_message(message.chat.id, "Мне кажется я не могу помочь с этим вопросом.")
            bot.send_message(message.chat.id, help_message)
    else:
        bot.send_message(message.chat.id, "Мне кажется я не могу помочь с этим вопросом.")
        bot.send_message(message.chat.id, help_message)

@bot.message_handler(func=lambda message: message.text == 'Закончить диалог')
def end_dialog(message):
    bot.send_message(message.chat.id, "Спасибо, что обратились ко мне с вашим вопросом, буду рад помочь в будущем!")

@bot.callback_query_handler(func=lambda call: True)
def callback_query(call):
    if call.data == "yes":
        chat_id = call.message.chat.id
        if chat_id in user_states:
            subtype = user_states[chat_id]['subtype']
            if subtype in questions_for_subtypes:
                first_question = questions_for_subtypes[subtype]['questions'][0]
                bot.send_message(chat_id, first_question)
                user_states[chat_id]['question_idx'] = 1
            else:
                bot.send_message(chat_id, "Sorry, I do not have further questions for this topic.")
        else:
            bot.send_message(chat_id, "It seems there was an error. Can you please ask your question again?")
    elif call.data == "no":
        bot.send_message(call.message.chat.id, help_message)

